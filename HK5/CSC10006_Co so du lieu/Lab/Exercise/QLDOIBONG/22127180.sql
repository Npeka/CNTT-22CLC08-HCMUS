CREATE DATABASE QLDOIBONG
GO

USE QLDOIBONG
GO

CREATE TABLE DOI
(
	IDDOI INT,
	TENDOI NVARCHAR(50),
	DOITRUONG INT,
	SOLUONG INT

	CONSTRAINT PK_DOI
	PRIMARY KEY(IDDOI)
)

CREATE TABLE BOTRI
(
	IDDOI INT,
	IDTHANHVIEN INT,
	DIACHI NVARCHAR(100),
	NHIEMVU NVARCHAR(100),
	QUANLI INT

	CONSTRAINT PK_BOTRI
	PRIMARY KEY(IDDOI, IDTHANHVIEN)
)

CREATE TABLE THANHVIEN
(
	IDTHANHVIEN INT,
	HOTEN NVARCHAR(50),
	SOCMND CHAR(9),
	DIACHI NVARCHAR(100),
	NGAYSINH DATE

	CONSTRAINT PK_THANHVIEN
	PRIMARY KEY(IDTHANHVIEN)
)

ALTER TABLE BOTRI
ADD 
	CONSTRAINT FK_BOTRI_IDDOI
	FOREIGN KEY(IDDOI)
	REFERENCES DOI,

	CONSTRAINT FK_BOTRI_IDTHANHVIEN
	FOREIGN KEY(IDTHANHVIEN)
	REFERENCES THANHVIEN,

	CONSTRAINT FK_BOTRI_QUANLI
	FOREIGN KEY(QUANLI)
	REFERENCES THANHVIEN

ALTER TABLE DOI 
ADD
	CONSTRAINT FK_DOI_DOITRUONG
	FOREIGN KEY(DOITRUONG)
	REFERENCES THANHVIEN

